#include <iostream>
using namespace std;
int main()
{
	cout << 6 + 5/7 + 8%2 << endl;
	cout << 6 + 1.0*5/7 + 8%2 << endl;
	cout << 6*6 + 4*7 << endl;
	cout << 6%2 + 4%3 << endl;
	cout << 19%(17%15) << endl;
	cout << 19%17%15 << endl;
	cout << 8 % -3 << endl;
	cout << -8%3 << endl;
	cout << -8%-3 << endl;
	cout << 8.0%3 << endl;
}
